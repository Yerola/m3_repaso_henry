DROP DATABASE IF EXISTS Bookstore;
CREATE DATABASE IF NOT EXISTS Bookstore;

-- ==========================================================
-- Seleccionar la Base de Datos
-- ==========================================================

USE Bookstore;

-- ==========================================================
-- Eliminar las tablas en caso existan
-- ==========================================================

DROP TABLE IF EXISTS Venta;
DROP TABLE IF EXISTS Publicacion;
DROP TABLE IF EXISTS Tipo;
DROP TABLE IF EXISTS Usuario;
DROP TABLE IF EXISTS Cliente;
DROP TABLE IF EXISTS Empleado;
DROP TABLE IF EXISTS Promocion;
DROP TABLE IF EXISTS Canal_Venta;

   
-- ==========================================================
-- Creación de la Tablas
-- ==========================================================

CREATE TABLE Tipo (
   id                   VARCHAR(3) NOT NULL,
   descripcion          VARCHAR(50) NOT NULL,
   PRIMARY KEY (id)
)  ENGINE = INNODB DEFAULT CHARSET=UTF8MB4 COLLATE = UTF8MB4_SPANISH_CI;

CREATE TABLE Promocion (
   id                   INTEGER AUTO_INCREMENT,
   tipo                 VARCHAR(3) NOT NULL,
   cant_min             INTEGER NOT NULL,
   cant_max             INTEGER NOT NULL,
   porcentaje           DECIMAL(4,2) NOT NULL,
PRIMARY KEY (Id)
)  ENGINE = INNODB DEFAULT CHARSET=UTF8MB4 COLLATE = UTF8MB4_SPANISH_CI;

CREATE TABLE Publicacion (
   id                   INTEGER AUTO_INCREMENT,
   titulo               VARCHAR(100) NOT NULL,
   autor                VARCHAR(100) NOT NULL,
   nro_edicion          INTEGER NOT NULL,
   precio               DECIMAL(10,2) NOT NULL,
   stock                INTEGER NOT NULL,
   costo                DECIMAL(10,2) NOT NULL,
   id_tipo              VARCHAR(3) NOT NULL,
   PRIMARY KEY (id)
)  ENGINE = INNODB DEFAULT CHARSET=UTF8MB4 COLLATE = UTF8MB4_SPANISH_CI;

CREATE TABLE Cliente (
   id                   INTEGER AUTO_INCREMENT,
   documento            INTEGER NOT NULL,
   nombre               VARCHAR(50) NOT NULL,
   direccion            VARCHAR(50) NOT NULL,
   telefono             VARCHAR(50) NOT NULL,
   PRIMARY KEY (id)
) ENGINE = INNODB DEFAULT CHARSET=UTF8MB4 COLLATE = UTF8MB4_SPANISH_CI;

CREATE TABLE Empleado (
   id                   INTEGER AUTO_INCREMENT,
   apellido             VARCHAR(50) NOT NULL,
   nombre               VARCHAR(50) NOT NULL,
   direccion            VARCHAR(100) NOT NULL,
   email                VARCHAR(50) NOT NULL,
   PRIMARY KEY (id)
) ENGINE = INNODB DEFAULT CHARSET=UTF8MB4 COLLATE = UTF8MB4_SPANISH_CI;

CREATE TABLE Venta (
   id                   INTEGER AUTO_INCREMENT,
   fecha                DATE NOT NULL,
   id_empleado          INTEGER NOT NULL,
   id_canal	            INTEGER  DEFAULT 1,
   id_publicacion       INTEGER NOT NULL,
   cantidad             INTEGER NOT NULL,
   precio               DECIMAL(10,2) NOT NULL,
   id_promocion         INTEGER DEFAULT 0,
   PRIMARY KEY (id)
)  ENGINE = INNODB DEFAULT CHARSET=UTF8MB4 COLLATE = UTF8MB4_SPANISH_CI;

CREATE TABLE Canal_Venta (
   id            		INTEGER AUTO_INCREMENT,
   nombre               VARCHAR(50) NOT NULL,
   PRIMARY KEY (id)
) ENGINE = INNODB DEFAULT CHARSET=UTF8MB4 COLLATE = UTF8MB4_SPANISH_CI;


